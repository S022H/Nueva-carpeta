def valor_absoluto(numero):
    """
    Calcula el valor absoluto de un número.

    Args:
        numero (float): El número para el cual se calculará el valor absoluto.

    Returns:
        float: El valor absoluto del número.
    """
    return abs(numero)

# Ejemplos de uso
print(f"El valor absoluto de 2 es {valor_absoluto(2)}")
print(f"El valor absoluto de -4 es {valor_absoluto(-4)}")
